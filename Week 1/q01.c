// Converting a while loop into a for loop

#include <stdio.h>

int main(int argc, char **argv) {
    // While Loop
    // int i = 10;
    // while (i >= 0) {
    //     printf("%d\n", i);
    //     i--;
    // }

    // For Loop
    for (int i = 10; i >= 0; i--) {
        printf("%d\n", i);
    }

    return 0;
}
